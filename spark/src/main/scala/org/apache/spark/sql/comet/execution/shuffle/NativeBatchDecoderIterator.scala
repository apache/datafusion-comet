package org.apache.spark.sql.comet.execution.shuffle

import java.io.InputStream

import org.apache.spark.sql.execution.metric.SQLMetric
import org.apache.spark.sql.vectorized.ColumnarBatch

import org.apache.comet.Native
import org.apache.comet.vector.NativeUtil

/**
 * This iterator wraps a Spark input stream that is reading shuffle blocks generated by the Comet
 * native ShuffleWriterExec and then calls native code to decompress and decode the shuffle blocks
 * and use Arrow FFI to return the Arrow record batch.
 */
case class NativeBatchDecoderIterator(
    in: InputStream,
    decodeTime: SQLMetric,
    nativeLib: Native,
    nativeUtil: NativeUtil,
    tracingEnabled: Boolean)
    extends Iterator[ColumnarBatch] {

  private var isClosed = false
  private var currentBatch: ColumnarBatch = null

  // Open the native stream reader
  private val handle: Long = if (in != null) {
    nativeLib.openShuffleStream(in)
  } else {
    0L
  }

  // Get field count from the native reader (it parsed the schema on open)
  private val numFields: Int = if (handle != 0L) {
    nativeLib.shuffleStreamNumFields(handle).toInt
  } else {
    0
  }

  private var batch = fetchNext()

  def hasNext(): Boolean = {
    if (handle == 0L || isClosed) {
      return false
    }
    if (batch.isDefined) {
      return true
    }

    if (currentBatch != null) {
      currentBatch.close()
      currentBatch = null
    }

    batch = fetchNext()
    if (batch.isEmpty) {
      close()
      return false
    }
    true
  }

  def next(): ColumnarBatch = {
    if (!hasNext) {
      throw new NoSuchElementException
    }
    val nextBatch = batch.get
    currentBatch = nextBatch
    batch = None
    currentBatch
  }

  private def fetchNext(): Option[ColumnarBatch] = {
    if (handle == 0L || isClosed) {
      return None
    }

    val startTime = System.nanoTime()
    val batch = nativeUtil.getNextBatch(
      numFields,
      (arrayAddrs, schemaAddrs) => {
        nativeLib.nextShuffleStreamBatch(handle, arrayAddrs, schemaAddrs)
      })
    decodeTime.add(System.nanoTime() - startTime)
    batch
  }

  def close(): Unit = {
    synchronized {
      if (!isClosed) {
        if (currentBatch != null) {
          currentBatch.close()
          currentBatch = null
        }
        if (handle != 0L) {
          nativeLib.closeShuffleStream(handle)
        }
        if (in != null) {
          in.close()
        }
        isClosed = true
      }
    }
  }
}
